package com.bitmap.bean.service;

public class MechanicTimeLine {
	String id = "";
	String number = "";
	String labor_id = "";
	String labor_hour = "";
	String status = "";
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getLabor_id() {
		return labor_id;
	}
	public void setLabor_id(String labor_id) {
		this.labor_id = labor_id;
	}
	public String getLabor_hour() {
		return labor_hour;
	}
	public void setLabor_hour(String labor_hour) {
		this.labor_hour = labor_hour;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
